import greenfoot.*;  // (World, Actor, GreenfootImage, Greenfoot and MouseInfo)

public class Coordinates
{
    private int x;
    private int y;
    
    public Coordinates(int cx, int cy)
    {
        x = cx;
        y = cy;
    }  
    
    public int getX()
    {
        return x;
    }
    
    public int getY()
    {
        return y;
    }
}
